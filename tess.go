package main

import "fmt"

func main() {
    // Mencetak teks sederhana
    fmt.Println("Hello, Golang!")

    // Mencetak nilai variabel
    angka := 42
    fmt.Println("Nilai variabel angka:", angka)

    // Mencetak nilai variabel dengan format khusus
    floatNumber := 3.14
    fmt.Printf("Nilai variabel floatNumber: %.2f\n", floatNumber)
}
